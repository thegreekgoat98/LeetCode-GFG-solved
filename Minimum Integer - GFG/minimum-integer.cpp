class Array
{
public:
    template <class T>
    static void input(vector<T> &A,int n)
    {
        for (int i = 0; i < n; i++)
        {
            scanf("%d ",&A[i]);
        }
    }

    template <class T>
    static void print(vector<T> &A)
    {
        for (int i = 0; i < A.size(); i++)
        {
            cout << A[i] << " ";
        }
        cout << endl;
    }
};


// } Driver Code Ends
class Solution {
  public:
    int minimumInteger(int N, vector<int> &A) {
        // code here
        long long int sum=0,mini=INT_MAX;
        for(auto x:A) sum+=x;
        for(auto x:A){
            if((long long)N*x >= sum){
                mini=min(mini,1ll*x);
            }
        }
        return mini;
    }
};


