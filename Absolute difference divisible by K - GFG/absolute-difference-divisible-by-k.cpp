class Solution {
  public:
    long long countPairs(int n, int arr[], int k) 
    {
        unordered_map<long long,long long>mp;
        int cnt=0;
        for(int i=0;i<n;++i)
        {
            int rem=arr[i]%k;
            cnt+=mp[rem];
            mp[rem]++;
        }
        
        return cnt;
    }
};

